ALTER TABLE votes RENAME TO votes_old;

-- Create partitioned table
CREATE TABLE votes (
    id              BIGINT,
    post_id         BIGINT,
    vote_type_id    SMALLINT,
    creation_date   TIMESTAMP,
    user_id         BIGINT,
    bounty_amount   INT
) PARTITION BY RANGE (creation_date);

